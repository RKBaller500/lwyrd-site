async function run() {
  console.log("Deleting existing firm assessment items...");
  const { error: e1 } = await db.from("firm_assessment_items").delete().neq("firm_id", "");
  if (e1) console.error("  Error:", e1.message);
  else console.log("  Done.");

  console.log("Deleting existing attorneys...");
  const { error: e2 } = await db.from("attorneys").delete().neq("firm_id", "");
  if (e2) console.error("  Error:", e2.message);
  else console.log("  Done.");

  console.log("Deleting existing firms...");
  const { error: e3 } = await db.from("firms").delete().neq("id", "");
  if (e3) console.error("  Error:", e3.message);
  else console.log("  Done.");

  const BATCH = 25;
  console.log(`Inserting ${firms.length} firms in batches of ${BATCH}...`);
  for (let i = 0; i < firms.length; i += BATCH) {
    const batch = firms.slice(i, i + BATCH);
    const { error } = await db.from("firms").insert(batch);
    if (error) {
      console.error(`  Batch ${Math.floor(i / BATCH) + 1} error:`, error.message);
      process.exit(1);
    }
    console.log(`  Inserted firms ${i + 1}–${Math.min(i + BATCH, firms.length)}`);
  }

  console.log("\nAll 113 firms seeded successfully.");
}

run().catch((err) => {
  console.error(err);
  process.exit(1);
});
